"""
You can change every character in a string to uppercase by calling the upper method on it:
"""

print("We hold these truths...".upper())

"""
Similarly, you can change every letter in a string to lowercase by calling the lower method on it:
"""

print("SO IT GOES...".lower())

"""
You can capitalize the first letter of a sentence by calling the capitalize method on a string:
"""

print("four score and...".capitalize())